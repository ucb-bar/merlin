#!/usr/bin/env python3
"""Export golden test data in multiple formats for kernel writers.

Reads the .pt golden data produced by generate_npu_golden_tests.py and exports:
  1. NumPy .npy files (for non-PyTorch consumers)
  2. Raw binary files with shape metadata (for RTL/C testing)
  3. A summary report per kernel with shapes, dtypes, and golden values

Usage:
    python tools/export_golden_data.py benchmarks/SaturnNPU/golden_data/ \
        --formats numpy raw
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

import numpy as np
import torch


def pt_to_numpy(tensor: torch.Tensor) -> np.ndarray:
    """Convert a PyTorch tensor to numpy, handling bf16."""
    if tensor.dtype == torch.bfloat16:
        return tensor.float().numpy()
    if tensor.dtype == torch.float8_e4m3fn:
        return tensor.float().numpy()
    return tensor.numpy()


def export_scale_dir(pt_dir: Path, np_dir: Path, raw_dir: Path, do_numpy: bool, do_raw: bool):
    """Export one scale directory (small or real) of golden data."""
    if not pt_dir.exists():
        return

    for layer_dir in sorted(pt_dir.iterdir()):
        if not layer_dir.is_dir():
            continue

        meta_path = layer_dir / "metadata.json"
        if not meta_path.exists():
            continue

        with open(meta_path) as f:
            meta = json.load(f)

        layer_type = meta["layer_type"]
        print(f"  Exporting {layer_type}...")

        # Export layer-level I/O
        for pt_file in sorted(layer_dir.glob("*.pt")):
            tensor = torch.load(pt_file, weights_only=True)
            name = pt_file.stem

            if do_numpy:
                out_dir = np_dir / layer_type
                out_dir.mkdir(parents=True, exist_ok=True)
                np.save(out_dir / f"{name}.npy", pt_to_numpy(tensor))

            if do_raw:
                out_dir = raw_dir / layer_type
                out_dir.mkdir(parents=True, exist_ok=True)
                arr = pt_to_numpy(tensor)
                arr.tofile(out_dir / f"{name}.bin")
                # Write shape metadata
                with open(out_dir / f"{name}.meta", "w") as mf:
                    json.dump(
                        {
                            "shape": list(arr.shape),
                            "dtype": str(arr.dtype),
                            "original_dtype": str(tensor.dtype),
                            "nbytes": arr.nbytes,
                        },
                        mf,
                        indent=2,
                    )

        # Export operator-level data
        ops_dir = layer_dir / "operators"
        if not ops_dir.exists():
            continue

        for op_dir in sorted(ops_dir.iterdir()):
            if not op_dir.is_dir():
                continue

            for pt_file in sorted(op_dir.glob("*.pt")):
                tensor = torch.load(pt_file, weights_only=True)
                name = pt_file.stem
                rel_path = f"{layer_type}/operators/{op_dir.name}"

                if do_numpy:
                    out = np_dir / rel_path
                    out.mkdir(parents=True, exist_ok=True)
                    np.save(out / f"{name}.npy", pt_to_numpy(tensor))

                if do_raw:
                    out = raw_dir / rel_path
                    out.mkdir(parents=True, exist_ok=True)
                    arr = pt_to_numpy(tensor)
                    arr.tofile(out / f"{name}.bin")
                    with open(out / f"{name}.meta", "w") as mf:
                        json.dump(
                            {
                                "shape": list(arr.shape),
                                "dtype": str(arr.dtype),
                                "original_dtype": str(tensor.dtype),
                                "nbytes": arr.nbytes,
                            },
                            mf,
                            indent=2,
                        )

        # Copy metadata
        if do_numpy:
            (np_dir / layer_type / "metadata.json").write_text(meta_path.read_text())
        if do_raw:
            (raw_dir / layer_type / "metadata.json").write_text(meta_path.read_text())


def generate_kernel_summary(golden_dir: Path, output_path: Path):
    """Generate a comprehensive summary for kernel writers."""
    manifest_path = golden_dir / "golden_manifest.json"
    if not manifest_path.exists():
        print(f"  No golden_manifest.json found in {golden_dir}")
        return

    with open(manifest_path) as f:
        manifest = json.load(f)

    lines = []
    lines.append("# SmolVLA Kernel Implementation Guide")
    lines.append("")
    lines.append("This file is auto-generated by `tools/export_golden_data.py`.")
    lines.append("Golden data generated by `tools/generate_npu_golden_tests.py`.")
    lines.append("")
    lines.append("## How to use this data")
    lines.append("")
    lines.append("Each layer type has:")
    lines.append("- `layer_input_*.{pt,npy,bin}` — layer input tensors")
    lines.append("- `layer_output_*.{pt,npy,bin}` — expected layer output (ground truth)")
    lines.append("- `operators/NN_name/` — each operator in the decomposition")
    lines.append("  - `input_*.{pt,npy,bin}` — operator input")
    lines.append("  - `output_*.{pt,npy,bin}` — expected operator output")
    lines.append("")
    lines.append("**Composition**: chaining all operator outputs reproduces the layer output.")
    lines.append("This is verified at generation time (composition_verified=true).")
    lines.append("")
    lines.append("## Layer Decompositions")
    lines.append("")

    for trace in manifest.get("traces", []):
        label = trace["label"]
        lt = trace["layer_type"]
        cfg = trace["config"]
        ops = trace["operators"]

        lines.append(f"### {label}")
        lines.append(f"- **Layer type**: `{lt}`")
        lines.append(f"- **Config**: {json.dumps(cfg)}")
        lines.append(f"- **Operators** ({len(ops)}):")
        lines.append("")
        lines.append("| # | Operator | Type | PyTorch equivalent |")
        lines.append("|---|----------|------|-------------------|")

        pytorch_map = {
            "rms_norm": "GemmaRMSNorm / F.layer_norm",
            "layer_norm": "F.layer_norm",
            "q_proj": "nn.Linear (Q projection)",
            "k_proj": "nn.Linear (K projection)",
            "v_proj": "nn.Linear (V projection)",
            "o_proj": "nn.Linear (O projection)",
            "fc1": "nn.Linear (MLP expansion)",
            "fc2": "nn.Linear (MLP contraction)",
            "gate_proj": "nn.Linear (gate projection)",
            "up_proj": "nn.Linear (up projection)",
            "down_proj": "nn.Linear (down projection)",
            "rope": "GemmaRotaryEmbedding.apply_rotary_pos_emb",
            "score_matmul": "torch.matmul(Q, K^T) * scale",
            "softmax": "F.softmax(scores, dim=-1)",
            "value_matmul": "torch.matmul(attn_weights, V)",
            "gelu": "F.gelu(x, approximate='tanh')",
            "gate_mul_up": "gate * up (GeGLU combine)",
            "residual_add": "x + sublayer_output",
        }

        for idx, op in enumerate(ops):
            pytorch = pytorch_map.get(op["name"], "")
            lines.append(f"| {idx} | `{op['name']}` | `{op['op_type']}` | {pytorch} |")

        lines.append("")

    lines.append("## Which MLIR level to target")
    lines.append("")
    lines.append("Use **module.4.global-optimization.mlir** as your primary reference:")
    lines.append("- Transposes eliminated (774 → 0)")
    lines.append("- MX fp8 dequantization hoisted to compile-time")
    lines.append("- Attention + softmax already lowered to NPU ISA")
    lines.append("- What remains = the kernels YOU need to implement")
    lines.append("")
    lines.append("Use **module.1.input.mlir** to understand:")
    lines.append("- Which PyTorch operation each MLIR pattern came from")
    lines.append("- The full dequant chain before compiler optimization")
    lines.append("")

    output_path.write_text("\n".join(lines))
    print(f"  Kernel guide: {output_path}")


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("golden_dir", type=Path, help="Golden data directory")
    parser.add_argument(
        "--formats",
        nargs="+",
        choices=["numpy", "raw"],
        default=["numpy"],
        help="Export formats",
    )
    args = parser.parse_args()

    do_numpy = "numpy" in args.formats
    do_raw = "raw" in args.formats

    for scale in ["small", "real"]:
        pt_dir = args.golden_dir / scale
        if not pt_dir.exists():
            continue

        print(f"\nExporting {scale} scale golden data...")
        np_dir = args.golden_dir / f"{scale}_numpy"
        raw_dir = args.golden_dir / f"{scale}_raw"

        export_scale_dir(pt_dir, np_dir, raw_dir, do_numpy, do_raw)

    # Generate kernel writer's guide
    print("\nGenerating kernel implementation guide...")
    generate_kernel_summary(args.golden_dir, args.golden_dir / "KERNEL_GUIDE.md")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
