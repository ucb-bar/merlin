#!/usr/bin/env python3
"""Build the RVV-vs-OPU speedup table from firesim_v128d64_results.csv.

Emits both a LaTeX (\\begin{tabular}) file for the paper and a Markdown
mirror for the dev blog. The CSV has paired rows per matmul size (one
`OPU` row + one `RVV` row). We pair them by (M, N, K, hw_config).
"""

from __future__ import annotations

import argparse
import csv
from pathlib import Path

BENCH_DIR = Path(__file__).resolve().parent
DEFAULT_CSV = BENCH_DIR / "firesim_v128d64_results.csv"
DEFAULT_OUT_DIR = BENCH_DIR / "figures"


def load_rows(csv_path: Path) -> list[dict]:
    with open(csv_path) as f:
        return list(csv.DictReader(f))


def build_pairs(rows: list[dict]) -> list[dict]:
    """Return one row per (M,N,K) with RVV + OPU columns."""
    opu_by_shape: dict[tuple[int, int, int], dict] = {}
    rvv_by_shape: dict[tuple[int, int, int], dict] = {}
    for r in rows:
        key = (int(r["size_m"]), int(r["size_n"]), int(r["size_k"]))
        if r["variant"] == "OPU":
            opu_by_shape[key] = r
        elif r["variant"] == "RVV":
            rvv_by_shape[key] = r

    pairs = []
    for key in sorted(opu_by_shape.keys() & rvv_by_shape.keys()):
        opu = opu_by_shape[key]
        rvv = rvv_by_shape[key]
        pairs.append(
            {
                "M": key[0],
                "N": key[1],
                "K": key[2],
                "rvv_ops_per_cycle": float(rvv["ops_per_cycle"]),
                "opu_ops_per_cycle": float(opu["ops_per_cycle"]),
                "opu_utilization_pct": float(opu["utilization_pct"]),
                "speedup_vs_rvv": float(opu["speedup_vs_rvv"]),
                "hw_config": opu["hw_config"],
            }
        )
    return pairs


def emit_latex(pairs: list[dict], out: Path) -> None:
    """Emit a compact LaTeX tabular. Assumes the paper's preamble has
    booktabs and siunitx (falls back cleanly if not)."""
    lines = []
    lines.append("% Auto-generated by benchmarks/SaturnOPU/build_speedup_table.py")
    lines.append("% Source: benchmarks/SaturnOPU/firesim_v128d64_results.csv")
    lines.append("\\begin{tabular}{rrrrrr}")
    lines.append("\\toprule")
    lines.append(" M $\\times$ N $\\times$ K & RVV & OPU & Util. & Speedup \\\\")
    lines.append(" & (Ops/cyc) & (Ops/cyc) & (\\%) & ($\\times$) \\\\")
    lines.append("\\midrule")
    for p in pairs:
        lines.append(
            f" {p['M']}$\\times${p['N']}$\\times${p['K']} & "
            f"{p['rvv_ops_per_cycle']:.2f} & "
            f"{p['opu_ops_per_cycle']:.2f} & "
            f"{p['opu_utilization_pct']:.1f} & "
            f"{p['speedup_vs_rvv']:.1f} \\\\"
        )
    lines.append("\\bottomrule")
    lines.append("\\end{tabular}")
    out.write_text("\n".join(lines) + "\n")
    print(f"  â†’ {out}")


def emit_markdown(pairs: list[dict], out: Path) -> None:
    lines = []
    lines.append("<!-- Auto-generated; do not edit by hand. -->")
    lines.append("")
    lines.append("| M Ă— N Ă— K | RVV (Ops/cyc) | OPU (Ops/cyc) | Util. (%) | Speedup |")
    lines.append("|---:|---:|---:|---:|---:|")
    for p in pairs:
        lines.append(
            f"| {p['M']}Ă—{p['N']}Ă—{p['K']} | "
            f"{p['rvv_ops_per_cycle']:.2f} | "
            f"{p['opu_ops_per_cycle']:.2f} | "
            f"{p['opu_utilization_pct']:.1f} | "
            f"{p['speedup_vs_rvv']:.1f}Ă— |"
        )
    out.write_text("\n".join(lines) + "\n")
    print(f"  â†’ {out}")


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--csv", type=Path, default=DEFAULT_CSV)
    ap.add_argument("--out-dir", type=Path, default=DEFAULT_OUT_DIR)
    args = ap.parse_args()
    args.out_dir.mkdir(parents=True, exist_ok=True)

    rows = load_rows(args.csv)
    pairs = build_pairs(rows)
    emit_latex(pairs, args.out_dir / "speedup_table.tex")
    emit_markdown(pairs, args.out_dir / "speedup_table.md")


if __name__ == "__main__":
    main()
